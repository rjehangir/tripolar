int main(void)
{	
	setup();		
    while(1) loop();    
}


void setup(void)
{
	
	boardInit();
	cli();
	redOff();
	greenOn();
	millis_init();
	gimbal.begin();
}

void loop(void)
{
	static uint32_t lastTime = 0;
	static uint16_t currentSpeed = 0;
			
	if (_100us-lastTime >=20000)
	{
		lastTime = _100us;
		if (currentSpeed <10) currentSpeed++;
		else currentSpeed += currentSpeed / 10;
		gimbal.set_speed_rpm(currentSpeed);					
	}
		
	gimbal.tickle();	
}

